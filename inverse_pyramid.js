function formPyramid(n) {
    var x = (2 * n - 1);
    for (var i = 1; i <= n; i++) {
        var s = "";
        for (var j = 1; j <= x; j++) {
            if (j <= (x + 1 - i) && j >= i) {
                s += "*";
            }
            else {
                s += " ";
            }
        }
        console.log(s);
    }
}
formPyramid(10);